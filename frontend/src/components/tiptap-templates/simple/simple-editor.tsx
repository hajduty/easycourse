const MobileToolbarContent = ({
  type,
  onBack,
}: {
  type: "highlighter" | "link"
  onBack: () => void
}) => (
  <>
    <ToolbarGroup>
      <Button data-style="ghost" onClick={onBack}>
        <ArrowLeftIcon className="tiptap-button-icon" />
        {type === "highlighter" ? (
          <HighlighterIcon className="tiptap-button-icon" />
        ) : (
          <LinkIcon className="tiptap-button-icon" />
        )}
      </Button>
    </ToolbarGroup>

    <ToolbarSeparator />

    {type === "highlighter" ? (
      <ColorHighlightPopoverContent />
    ) : (
      <LinkContent />
    )}
  </>
)

export function SimpleEditor({ content, onChange, editable = true }: { content: any, onChange?: (newContent: any) => void, editable?: boolean }) {
  const isMobile = useIsBreakpoint()
  const { height } = useWindowSize()
  const [mobileView, setMobileView] = useState<"main" | "highlighter" | "link">(
    "main"
  )

  const toolbarRef = useRef<HTMLDivElement>(null)
  const [wordCount, setWordCount] = useState(0);

  const [ytDialogOpen, setYtDialogOpen] = useState(false)
  const [ytUrl, setYtUrl] = useState("")

  const insertYoutube = () => {
    if (!editor || !ytUrl) return
    editor.commands.setYoutubeVideo({
      src: ytUrl,
      width: 360,   // ignored by CSS
      height: 160,  // ignored by CSS
    })
    setYtUrl("")
    setYtDialogOpen(false)
  }

  const editor = useEditor({
    immediatelyRender: false,
    editorProps: {
      attributes: {
        autocomplete: "off",
        autocorrect: "off",
        autocapitalize: "off",
        "aria-label": "Main content area, start typing to enter text.",
        class: "simple-editor",
      },
    },
    extensions: [
      StarterKit.configure({
        horizontalRule: false,
        link: {
          openOnClick: false,
          enableClickSelection: true,
        },
      }
      ),
      Youtube.configure({
        controls: true,
        nocookie: true,
      }),
      HorizontalRule,
      TextAlign.configure({ types: ["heading", "paragraph"] }),
      TaskList,
      TaskItem.configure({ nested: true }),
      Highlight.configure({ multicolor: true }),
      Image,
      Typography,
      Superscript,
      Subscript,
      Selection,
      ImageUploadNode.configure({
        accept: "image/*",
        maxSize: MAX_FILE_SIZE,
        limit: 3,
        upload: handleImageUpload,
        onError: (error) => console.error("Upload failed:", error),
      }),
    ],
    content: content,
    onUpdate: ({ editor }) => {
      const text = editor.getText();
      const count = text.trim() === "" ? 0 : text.trim().split(/\s+/).length;
      setWordCount(count);
    },
    editable: editable,
  })

  useEffect(() => {
    if (!editor || !onChange) return;

    const handleUpdate = () => {
      const json = editor;
      onChange(json);
    };

    editor.on("update", handleUpdate);

    return () => {
      editor.off("update", handleUpdate);
    };
  }, [editor, onChange]);

  const rect = useCursorVisibility({
    editor,
    overlayHeight: toolbarRef.current?.getBoundingClientRect().height ?? 0,
  })

  useEffect(() => {
    if (!isMobile && mobileView !== "main") {
      setMobileView("main")
    }
  }, [isMobile, mobileView])

  return (
    <div className="bg-stone-950 h-full">
      <EditorContext.Provider value={{ editor }}>
        {editable &&
          <Toolbar
            ref={toolbarRef}
            style={{
              ...(isMobile
                ? {
                  bottom: `calc(100% - ${height - rect.y}px)`,
                }
                : {}),
            }}
          >
            <div className=" text-sm text-stone-400">
              {wordCount} words
            </div>
            {mobileView === "main" ? (
              <MainToolbarContent
                onHighlighterClick={() => setMobileView("highlighter")}
                onLinkClick={() => setMobileView("link")}
                isMobile={isMobile}
              />
            ) : (
              <MobileToolbarContent
                type={mobileView === "highlighter" ? "highlighter" : "link"}
                onBack={() => setMobileView("main")}
              />
            )}

            <ShadButton variant={'ghost'} className="text-xs" onClick={() => setYtDialogOpen(true)}>
              <TvMinimalPlay />
              Add Video
            </ShadButton>
            <AlertDialog open={ytDialogOpen} onOpenChange={setYtDialogOpen}>
              <AlertDialogContent className="dark text-white">
                <AlertDialogHeader>
                  <AlertDialogTitle>Insert YouTube Video</AlertDialogTitle>
                  <AlertDialogDescription>
                    Paste the YouTube video URL below.
                  </AlertDialogDescription>
                </AlertDialogHeader>
                <input
                  className="w-full border rounded px-2 py-1"
                  type="text"
                  placeholder="https://www.youtube.com/watch?v=..."
                  value={ytUrl}
                  onChange={(e) => setYtUrl(e.target.value)}
                />
                <AlertDialogFooter className="flex justify-end gap-2 mt-2">
                  <ShadButton onClick={() => setYtDialogOpen(false)}>Cancel</ShadButton>
                  <ShadButton onClick={insertYoutube}>Insert</ShadButton>
                </AlertDialogFooter>
              </AlertDialogContent>
            </AlertDialog>
          </Toolbar>
        }

        <EditorContent
          editor={editor}
          role="presentation"
          className="simple-editor-content h-full"
        />
      </EditorContext.Provider>
    </div>
  )
}
