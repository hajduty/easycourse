const MobileToolbarContent = ({
  type,
  onBack,
}: {
  type: "highlighter" | "link"
  onBack: () => void
}) => (
  <>
    <ToolbarGroup>
      <Button data-style="ghost" onClick={onBack}>
        <ArrowLeftIcon className="tiptap-button-icon" />
        {type === "highlighter" ? (
          <HighlighterIcon className="tiptap-button-icon" />
        ) : (
          <LinkIcon className="tiptap-button-icon" />
        )}
      </Button>
    </ToolbarGroup>

    <ToolbarSeparator />

    {type === "highlighter" ? (
      <ColorHighlightPopoverContent />
    ) : (
      <LinkContent />
    )}
  </>
)

export function SimpleEditor({ sectionId }: { sectionId: string }) {
  const isMobile = useIsBreakpoint();
  const { height } = useWindowSize();
  const [mobileView, setMobileView] = useState<"main" | "highlighter" | "link">("main");
  const toolbarRef = useRef<HTMLDivElement>(null);

  const [editor, setEditor] = useState<Editor | null>(null);
  const [content, setContent] = useState<any>("x"); // default to empty string

  const updateSection = useUpdateSection();

  useEffect(() => {
    const raw = localStorage.getItem(`editorContent:${sectionId}`);
    if (!raw || raw === "undefined" || raw === "null") {
      setContent("x"); 
      return;
    }

    try {
      setContent(JSON.parse(raw));
    } catch {
      setContent("x");
    }
  }, [sectionId]);

  useEffect(() => {
    if (!content) {
      return; // wait for content to be loaded
    }

    // destroy previous editor
    if (editor) {
      editor.destroy();
    }

    const newEditor = new Editor({
      extensions: [
        StarterKit.configure({
          horizontalRule: false,
          link: { openOnClick: false, enableClickSelection: true },
        }),
        HorizontalRule,
        TextAlign.configure({ types: ["heading", "paragraph"] }),
        TaskList,
        TaskItem.configure({ nested: true }),
        Highlight.configure({ multicolor: true }),
        Image,
        Typography,
        Superscript,
        Subscript,
        Selection,
        ImageUploadNode.configure({
          accept: "image/*",
          maxSize: MAX_FILE_SIZE,
          limit: 3,
          upload: handleImageUpload,
          onError: (error) => console.error("Upload failed:", error),
        }),
      ],
      content,
    });

    const save = () => {
      if (!newEditor.view) return;
      localStorage.setItem(`editorContent:${sectionId}`, JSON.stringify(newEditor.getJSON()));
    };
    newEditor.on("update", save);

    setEditor(newEditor);

    return () => {
      newEditor.destroy();
    };
  }, [content, sectionId]);

  if (!editor) return null;

  return (
    <div className="bg-stone-950">
      <EditorContext.Provider value={{ editor }}>
        <Toolbar ref={toolbarRef} style={{ ...(isMobile ? { bottom: `calc(100% - ${height}px)` } : {}) }}>
          {mobileView === "main" ? (
            <MainToolbarContent
              onHighlighterClick={() => setMobileView("highlighter")}
              onLinkClick={() => setMobileView("link")}
              isMobile={isMobile}
            />
          ) : (
            <MobileToolbarContent
              type={mobileView === "highlighter" ? "highlighter" : "link"}
              onBack={() => setMobileView("main")}
            />
          )}
        </Toolbar>

        <EditorContent editor={editor} role="presentation" className="simple-editor-content" />
      </EditorContext.Provider>
    </div>
  );
}